    				<p>一条轮胎的生产要经过很多步骤，最后一步是生胎的硫化成型。如果这一步失败，就会损失很多价值。因此，一款高效的脱模剂能提供最佳的硫化条件。</p>
    				<h3>二、技术介绍  </h3>
    				<p>脱模剂是一种在轮胎硫化时介于胶囊和轮胎之间的功能性物质，起到润滑、隔离和脱模的作用。一般以交联型改性聚硅氧烷和大分子有机物为主，另外加入功能性助剂。在高温状态下，即可在金属模具和轮胎之间进行自交联，形成弹性体薄膜，使产品内表面不粘、光滑、饱满、不结垢，不污染环境和其他隔离工具，是无毒无害的水性环保产品。</p>
    				<p>脱模剂 – 轮胎生产企业必备的化工助剂，主要是有利于橡胶和模具的分离，避免产生难于脱模导致的产品损坏或者质量不达标。由上海化工研究院和国家有机硅重点实验室联合开发。</p>
